    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {

            Repository repo = RepoFactory.getRepository();
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            Date date = new Date();

            User auth = (User) request.getSession().getAttribute("auth");

            if (auth != null) {
                String productId = request.getParameter("id");
                int productQuantity = Integer.parseInt(request.getParameter("quantity"));
                if (productQuantity <= 0) {
                    productQuantity = 1;
                }
                Game game=new Game();
                try {
                     game= repo.selectGame(Integer.parseInt(productId));
                } catch (Exception ex) {
                    Logger.getLogger(OrderNowServlet.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                Order orderModel = new Order();
                orderModel.setId(Integer.parseInt(productId));
                orderModel.setUserid(auth.getId());
                orderModel.setCategory(game.getCategory());
                orderModel.setQuantity(productQuantity);
                orderModel.setPrice(game.getPrice());
                orderModel.setPaymentMethod("PouzeÄ‡e");
                orderModel.setDate(formatter.format(date));

                try {
                    repo.CreateOrder(orderModel);
                    ArrayList<Cart> cart_list = (ArrayList<Cart>) request.getSession().getAttribute("cart-list");
                    if (cart_list != null) {
                        for (Cart c : cart_list) {
                            if (c.getId() == Integer.parseInt(productId)) {
                                cart_list.remove(cart_list.indexOf(c));
                                break;
                            }
                        }
                    }
                    response.sendRedirect("orders.jsp");

                } catch (Exception ex) {
                    Logger.getLogger(OrderNowServlet.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                response.sendRedirect("login.jsp");
            }

        }
    }

