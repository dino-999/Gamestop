package hr.algebra.model;

/**
 *
 * @author Dino
 */
public class User {

    private int id;
    private String email;
    private String password;
    private Role role;

    public User(int id, String email, String pass, Role role) {
        this.id = id;
        this.email = email;
        this.password = pass;
        this.role = role;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User(String email, String password) {

        this.email = email;
        this.password = password;
    }

    public User() {

    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
